# [67] 二进制求和
#

# @lc code=start
class Solution:
    # bitwise需要python的高精度整数
    def addBinary(self, a: str, b: str) -> str:
        x, y = int(a, 2), int(b, 2)
        while y:
            answer = x ^ y
            carry = (x & y) << 1
            x, y = answer, carry
        return bin(x)[2:]


